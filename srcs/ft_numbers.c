#include "../includes/ft_printf.h"

void	ft_number(int number, int *len)
{
	if ( number == -2147483648)
	{
		write(1, "-2147483648", 11);
			(len) += 11;
			return ;
	}
	if (number < 0)
	{
		ft_putcharacter_len('-', len);
		ft_number(number * -1, len);
	}
	else
	{
		
	}
}
